<h3>ONGC Represents India's Energy Security Through its Pioneering Efforts</h3>
<p>ONGC is the only fully–integrated petroleum company in India, operating along the entire hydrocarbon value chain. It has single-handedly scripted India's hydrocarbon saga. Some key pointers:</p>
<ul>
<li>ONGC has discovered 6 out of the 7 producing basins in India:</li>
<li>It has 7.59 billion tonnes of In-place hydrocarbon reserves. It has to its credit more than 320 discoveries of oil and gas with Ultimate Reserves of 2.69 Billion Metric tonnes (BMT) of Oil Plus Oil Equivalent Gas (O+OEG) from domestic acreages.</li><img src="images/ongcindia/image_2.jpg" border="0" alt="Pioneering Efforts" title="Pioneering Efforts" align="right">
<li>It has cumulatively produced 851 Million Metric Tonnes (MMT) of crude and 532 Billion Cubic Meters (BCM) of Natural Gas, from 111 fields.</li>
<li>ONGC has won 121 out of a total 235 Blocks (more than 50%) in the 8 rounds of bidding, under the New Exploration Licensing Policy (NELP) of the Indian Government.</li>
<li>ONGC's wholly-owned subsidiary ONGC Videsh Ltd. (OVL) is the biggest Indian multinational, with 30 Oil &amp; Gas projects (9 of them producing) in 15 countries.</li>
<li>Produces over 1.24 million barrels of oil equivalent per day, contributing over 64% of India's domestic production. Of this, over 75% of crude oil produced is Light &amp; Sweet.</li>
<li>The Company holds the largest share of hydrocarbon acreages in India (51% in PEL Areas &amp; 67% in ML Areas).</li>
<li>ONGC possesses about one tenth of the total Indian refining capacity.</li>
<li>ONGC has a well-integrated Hydrocarbon Value Chain structure with interests in LNG and product transportation business as well.</li>
<li>A unique organization in world to have all operative offshore and onshore installations (403) accredited with globally recognized certifications.</li>
</ul>
<h3>Competitive Strength</h3>
<p><img src="images/ongcindia/Competitive_Strength_img.jpg" align="right"> </p>
<ul>
<li>All crudes are sweet and most (76%) are light, with sulphur percentage ranging from 0.02-0.10, API gravity range 26°-46° and hence attract a premium in the market.</li>
<li>Strong intellectual property base, information, knowledge, skills and experience </li>
<li>Maximum number of Exploration Licenses, including competitive NELP rounds. ONGC has bagged 121 of the 235 Blocks (more than 50%) awarded in the 8 rounds of NELP.</li>
<li>ONGC owns and operates more than 26,600 kilometers of pipelines in India, including sub-sea pipelines. No other company in India operates even 50 per cent of this route length.</li>
</ul>
<h3>Perspective Plan 2030 (PP2030) </h3>
<p><img src="images/ongcindia/Perspective_img.jpg" align="right"> PP2030 charts the roadmap for ONGC's growth over the next two decades. It aims to double ONGC's production over the plan period with 4-5 per cent growth against the present growth rate of 2 percent. In physical terms the aspirations under Perspective Plan 2030 aims for -</p>
<ul>
<li>Production of 130 mmtoe of oil and oil equivalent gas (O + OEG) per year and accretion of over 1,300 mmtoe of proven reserves.</li>
<li>Grow ONGC Videsh Limited (OVL) six fold to 60 mmtoe of international O+OEG production per year by 2030.</li>
<li>More than 20 mmtoe of O+OEG production per year in India coming from new unconventional sources such as shale gas, CBM, deepwater and HPHT (High Pressure &amp; High Temperature) reservoirs.</li>
<li>Over 6.5 GW power generation from nuclear, solar and wind and 9 MTPA of LNG.</li>
<li>Scaling up refining capacity to over 20 MMTPA and targeted investments to capture downstream integration in petrochemicals.</li>
</ul>
<h3>Sourcing Equity Oil Abroad
</h3>
<ul>
<p>ONGC Videsh Limited (OVL) is operating in 15 countries with 30 projects with cumulative investment worth over USD 15 billion, to source equity oil &amp; gas for energy security of the country. Over the years OVL has emerged as the biggest Indian Multinational.</p>
<li>The company now has participation in 30 E&amp;P projects in 15 countries namely Vietnam (1 project), Russia (2 projects), Sudan (2 projects), South Sudan(2 projects), Iraq (1 project), Libya (1 project), Myanmar (2 projects), Syria (2 projects), Cuba (2 projects), Brazil (4 projects), Nigeria (1 project), Colombia (6 projects), Venezuela (2 projects) and Kazakhstan (1 project). Out of 30 projects, ONGC Videsh is Operator in 9 projects and Joint Operator in 7 projects.</li>
<li><img src="images/ongcindia/Sourcing_Equity_img.jpg" align="right">OVL continued to maintain its robust growth with production of 6.214 MMT of Crude Oil and 2.539 BCM of Gas during 2011-12. Its proved reserves (1P) as on 1st April 2012 stood at 193.381 MTOE, which next to ONGC, is the second largest holding of proved oil and gas reserves by any Indian Company. OVL's share of total reserves (3P) of oil and oil equivalent gas as on 1st April 2012 was 425.941 MTOE. As on 31st March, 2012, the Reserves-to-Production (R/P) Ratio considering proved reserves was 22.09.</li>
<li>Consolidated gross revenue of OVL increased from Rs.186.711 billion in 2010-11 to Rs.226.314 billion in 2011-12, up 21.2% and consolidated net profit from Rs.26.91 billion in 2010-11 to Rs.27.21 billion in 2011-12. </li>
<li>OVL was accorded with Mini-Ratna Category-I status by Government of India during July 2011. Recently during September 2012, OVL has been upgraded from a Schedule "B" public enterprise to Schedule "A". </li>
<li>OVL signed agreements with KazMunaiGas (KMG), the national oil company of Kazakhstan for acquisition of 25% participative interest in Satpayev exploration block in Kazakhstan. The agreement was signed on 16th April 2011 with KazMunaiGas in the presence of Dr. Manmohan Singh, Hon'ble Prime Minister of India and H.E. Nursultan Nazarbayev, President of Kazakhstan.</li>
<li>OVL along with Petronas and Nilepet has signed a Transition Agreement on 13th January 2012 with the Government of RSS for the continuation of its right for petroleum exploration and exploitation in Block 5A. The partners of Block 5A have incorporated a new operating company SUDD Petroleum Operating Co. Ltd. (SPOC) registered in Mauritius on 7th March 2012. The block will now be jointly operated by all partners. </li>
<li>OVL signed definitive agreements during September 2012 for the acquisition of Hess Corporation's 2.7213% participating interest in the Azeri, Chirag and the Deep Water Portion of Guneshli Fields in the Azerbaijan sector of the Caspian Sea ('ACG') and 2.36% interest in the Baku-Tbilisi-Ceyhan Pipeline ('BTC'), for US$ 1 Billion. ACG, which is located in the south Caspian Sea about 95 km off the coast of Azerbaijan, is the largest oil and gas field complex in Azerbaijan and is one of the largest producing oil fields in the world with average daily production from the field around 700,000 bopd of crude oil. </li>
<li>OVL's strategic objective of sourcing 20 million tonnes of equity oil abroad per year is likely to be fulfilled by 2018. As per 'Perspective Plan 2030', OVL is eyeing a six fold increase in production by 2030; from about 9 MTOE in current fiscal to 60 MTOE per annum by the year 2030.</li>
</ul>
