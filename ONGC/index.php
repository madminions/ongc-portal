<h3>Financials (2011-12)</h3>
<p>ONGC group's turnover during 2011-12 has been Rs. 150,185 Crore with net profit of Rs. 28,144 Crore. ONGC paid the highest-ever dividend of Rs. 8,342 Crore. The Net Worth of ONGC Group of companies is Rs. 135,266 Crore.</p>
<ul>
<li>During 2011-12, the turnover of ONGC (on standalone basis) has been Rs. 76,887 Crore with net profit of Rs.25,123 Crore; the highest-ever despite sharing under-recovery of Rs.44,466 Crore to the Oil Marketing Companies (OMCs) as per the instructions of the Government of India. Net worth of ONGC (on standalone basis) has been Rs.1,11,784 Crore.</li>
<li>OVL's consolidated gross revenue increased by 21% from Rs. 18,671 Crore during 2010-11 to Rs.22,637 Crore during 2011-12 and consolidated net profit increased by 1% from Rs. 2,621 Crore during 2010-11 to Rs. 2,721 Crore during 2011-12.</li>
<li>The turnover of MRPL has been Rs.52,207 Crore, up 19% from Rs.43,800 Crore and net profit has been Rs.909 Crore during 2011-12. </li>
</ul>
<h3>The Road Ahead</h3>
<p>ONGC looks forward to become an integrated energy provider, with:</p>
<ul>
<li>New Discoveries and fast track development</li>
<li>Equity Oil from Abroad</li>
<li>Downstream Value Additions &amp; Forward Integration</li>
<li>Leveraging state-of-the art technology and global best practices</li>
<li>New Sources of Energy</li>
<li>Production from small and marginal fields</li>
<li>ONGC has taken structured initiatives to tap unconventional energy sources through unconventional gases like Coal Bed Methane (CBM), Underground Coal Gasification (UCG), Shale Gas and Gas Hydrates, or unconventional energy sources like wind, solar etc. </li>
<li>"ONGC Energy Centre Trust", a dedicated centre created by ONGC for holistic research in non-conventional energy sources, has taken up three projects viz., Thermo-chemical reactor for Hydrogen, Geo-bio Reactors and Fuel Cells. ONGC has already commissioned a 50 MW Wind Farm in Gujarat and plan is afoot to set up another 100 MW Wind Farm in Rajasthan. ONGC has also set up 3 Solar Thermal Engines at Solar Energy Centre, Ministry of New and Renewable Energy (MNRE) campus at Gurgaon.</li>
</ul>
<h3>Value-chain integration</h3>
<ul>
<li>ONGC's purchase of majority stake in equity in the ailing Mangalore Refinery &amp; Petrochemicals Limited (MRPL), a stand-alone refinery of 9.69 MMT capacity in March 2003 is a standout testimony of ONGC's integrated business model. Besides adding that desired comfort to this Company in mitigating higher risk of E&amp;P operation, this deal also set an example in the Indian business history where a PSU has taken over a joint stock company and turned it around in a record time of one year.</li>
<li>Moving ahead, ONGC has taken structured initiatives towards value-multiplier integration projects like – Refinery, LNG, Petrochemicals, Power, SEZ, etc., to have presence in the entire hydrocarbon value-chain.</li>
</ul>
